    }
    
    func updateScaleMultiplier(_ width: CGFloat, _ height: CGFloat) {
        self.scaleXMultiplier = width  / TentacleImage.baseWidth;
        self.scaleYMultiplier = height / TentacleImage.baseHeight;
        if self.scaleProportional {
            let newMultiplier: CGFloat
            if self.proportionalFill {
                newMultiplier = max(self.scaleXMultiplier, self.scaleYMultiplier)
            } else {
                newMultiplier = min(self.scaleXMultiplier, self.scaleYMultiplier)
            }
            self.scaleXMultiplier = newMultiplier
            self.scaleYMultiplier = newMultiplier
        }
        
        let transform = NSAffineTransform.init()
        transform.scaleX(by: self.scaleXMultiplier, yBy: self.scaleYMultiplier)
        
        tentacleShapePathLayer.path = transform.transform(tentacleShapePath).cgPath
        tentacleSkinPathLayer.path = transform.transform(tentaclePath).cgPath
        tentacleSkinLightPathLayer.path = transform.transform(middleTentaclePath).cgPath
        tentaclePatternPathLayer.path = transform.transform(patternPath).cgPath
        tentacleSuctionCupPathLayer.path = transform.transform(suctionCupPath).cgPath
    }
    
